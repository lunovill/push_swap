#include "push_swap.h"

int	op_rotate_all(t_pile *p1, t_pile *p2)
{
	if (!p1 || !p2)
		return (-1);
	op_rotate(p1, 0);
	op_rotate(p2, 0);
	ft_putstr("rr\n");
	return (0);
}
