#include "push_swap.h"

void	ft_three(t_pile *a)
{
	if ((a->first->id == 1 && a->last->id == 2)
		|| (a->first->id == 2 && a->last->id == 0)
		|| (a->first->id == 0 && a->last->id == 1))
		op_swap(a);
}
